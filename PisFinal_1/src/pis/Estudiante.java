    public static void construirTabla(String[][] registro){
DefaultTableModel modelo = (DefaultTableModel)registroTabla.getModel();
modelo.setRowCount(registro.length);
modelo.setColumnCount(9);
JTableHeader head = registroTabla.getTableHeader();
TableColumnModel tcm = head.getColumnModel();
TableColumn tabCM = tcm.getColumn(0);
tabCM.setHeaderValue("Nombres");
TableColumn tabCM2 = tcm.getColumn(1);
tabCM2.setHeaderValue("Apellidos");
TableColumn tabCM3 = tcm.getColumn(2);
tabCM3.setHeaderValue("Cédula");
TableColumn tabCM4= tcm.getColumn(3);
tabCM4.setHeaderValue("Edad");
TableColumn tabCM5= tcm.getColumn(4);
tabCM5.setHeaderValue("Dia");
TableColumn tabCM6= tcm.getColumn(5);
tabCM6.setHeaderValue("Mes");
TableColumn tabCM7= tcm.getColumn(6);
tabCM7.setHeaderValue("Anio");
TableColumn tabCM8 = tcm.getColumn(7);
tabCM8.setHeaderValue("Télefono");
TableColumn tabCM9= tcm.getColumn(8);
tabCM9.setHeaderValue("Dirección");
for(int i=0;i<registro.length;i++){
for(int j=0;j<9;j++){
modelo.setValueAt(registro[i][j], i, j);
}
}
}
