    private void buscarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBActionPerformed
        
        Datos estudiante=new Datos("","","","","","","","","",0);
        leerArchivo();
    switch(criteriosCB.getSelectedItem().toString()){
        case "Cédula":
        estudiante=buscarCedula(tBusqueda.getText());
        break;
        case "Nombres":
        estudiante=buscarNombre(tBusqueda.getText());
        break;
        case "Apellidos":
        estudiante=buscarApellido(tBusqueda.getText());
        break;
    }
    if(estudiante.getCedula().length()==0 && criteriosCB.getSelectedItem()=="Cédula"){
       JOptionPane.showMessageDialog(this,
        "No se encontró ningun alumno con esa cédula.", "Error",
        JOptionPane.ERROR_MESSAGE);
    }
    if(estudiante.getNombre().length()==0 && criteriosCB.getSelectedItem()=="Nombres"){
       JOptionPane.showMessageDialog(this,
        "No se encontró ningun alumno con esa nombre.", "Error",
        JOptionPane.ERROR_MESSAGE);
    }
    if(estudiante.getApellido().length()==0 && criteriosCB.getSelectedItem()=="Apellidos"){
       JOptionPane.showMessageDialog(this,
        "No se encontró ningun alumno con esa apellido.", "Error",
        JOptionPane.ERROR_MESSAGE);
    }
    if(estudiante.getCedula()!=""){
    bEliminarAlumno.setVisible(true);
    }
    tCedula.setText(estudiante.getCedula());
    tNombre.setText(estudiante.getNombre());
    tApellido.setText(estudiante.getApellido());
    Dia.setSelectedItem(estudiante.getDia());
    Mes.setSelectedItem(estudiante.getMes());
    Anio.setSelectedItem(estudiante.getAnio());
    Edad.setText(estudiante.getEdad());
    tTelefono.setText(estudiante.getTelefono());
    tDireccion.setText(estudiante.getDireccion());
    }//GEN-LAST:event_buscarBActionPerformed

